#!/usr/bin/env bash
# orchestrate-sweep.sh — drives the full RAM-fit matrix SERIALLY (one capped
# process at a time) via run-sweep-anon.sh. Appends to sweep-ram.jsonl.
#
# Matrix (from the task spec):
#   Goal A (MiniLM quality, target 1024m): A1 A2 A3 A4 A5
#   Goal B (target 512m, min quality loss): B1 B2 B3 B4 B5
#   Plus: re-run best Goal-B MiniLM + static @1024m for headroom.
#
# For each config we sweep BOTH query and write modes over the cap ladder
# {1024M,512M,320M} (ladder stops at first OOM). Finalists also get a
# concurrent (sustained burst) run at the lowest fitting cap + a floor push.
#
# A4 (fp16 MiniLM) is recorded but expected to FAIL at runtime on this
# onnxruntime build (InsertedPrecisionFree graph error) per prep — captured
# as an error row, not an OOM.

set -uo pipefail
RUN="${HOME}/munin-ramfit/repo/benchmark/ramfit/run-sweep-anon.sh"
CACHE="${HOME}/munin-ramfit/hf-cache"

MINILM="Xenova/all-MiniLM-L6-v2"
STATIC="Xenova/bge-small-en-v1.5"

SQLITE_DEFAULT=(MUNIN_SQLITE_CACHE_KIB=4096 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=2)

echo "############ RAM-FIT MATRIX START $(date -u +%FT%TZ) ############"

# ── Goal A — MiniLM quality, target 1024m. Full cap ladder to find the floor. ──
echo "########## GOAL A ##########"

# A1 fp32 batch=4 sqlite=default
bash "$RUN" query A1 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}"
bash "$RUN" write A1 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}"

# A2 q8 batch=4 sqlite=default
bash "$RUN" query A2 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}"
bash "$RUN" write A2 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}"

# A3 q8 batch=1 cache=2048 mmap=0 OMP=1
bash "$RUN" query A3 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=2048 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
bash "$RUN" write A3 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=2048 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1

# A4 fp16 batch=4 sqlite=default — expected runtime-unsupported on this onnxruntime build
bash "$RUN" query A4 1024M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=fp16 MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}"
bash "$RUN" write A4 1024M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=fp16 MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}"

# A5 int8 batch=4 sqlite=default
bash "$RUN" query A5 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=int8 MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}"
bash "$RUN" write A5 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=int8 MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}"

# ── Goal B — target 512m, find min-quality-loss that fits. ────────────────────
echo "########## GOAL B ##########"

# B1 q8 batch=1 cache=1024 mmap=0 OMP=1
bash "$RUN" query B1 512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
bash "$RUN" write B1 512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1

# B2 int8 batch=1 cache=1024 mmap=0 OMP=1
bash "$RUN" query B2 512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=int8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
bash "$RUN" write B2 512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=int8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1

# B3 STATIC(bge-small q8) batch=1 cache=1024 mmap=0 OMP=1
bash "$RUN" query B3 512M,320M MUNIN_EMBEDDINGS_MODEL=$STATIC MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
bash "$RUN" write B3 512M,320M MUNIN_EMBEDDINGS_MODEL=$STATIC MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1

# B4 STATIC(bge-small q8) batch=1 cache=512 mmap=0 OMP=1
bash "$RUN" query B4 512M,320M MUNIN_EMBEDDINGS_MODEL=$STATIC MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=512 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
bash "$RUN" write B4 512M,320M MUNIN_EMBEDDINGS_MODEL=$STATIC MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=512 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1

# B5 EMBEDDINGS_ENABLED=false (lexical-only floor) — model not loaded
bash "$RUN" query B5 512M,320M MUNIN_EMBEDDINGS_ENABLED=false MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
# (write mode for B5: insert + worker that does nothing; still informative for the sidecar floor)
bash "$RUN" write B5 512M,320M MUNIN_EMBEDDINGS_ENABLED=false MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1

echo "########## HEADROOM RE-RUNS @1024m ##########"
# best Goal-B MiniLM (B1: q8 batch=1) @1024m
bash "$RUN" query B1-1024 1024M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
bash "$RUN" write B1-1024 1024M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
# static (B3) @1024m
bash "$RUN" query B3-1024 1024M MUNIN_EMBEDDINGS_MODEL=$STATIC MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
bash "$RUN" write B3-1024 1024M MUNIN_EMBEDDINGS_MODEL=$STATIC MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1

echo "########## FLOOR PUSH (finalists below 320m) ##########"
# Push the cap LOWER than 320m to find the true anon floor for the cheapest configs.
# Ladder 256M,192M,160M,128M; stops at first OOM.
# fp32 MiniLM (full quality) floor — query
bash "$RUN" query FLOOR-fp32 256M,192M,160M,128M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}"
# q8 MiniLM floor — query
bash "$RUN" query FLOOR-q8 256M,192M,160M,128M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
# fp32 MiniLM floor — write (worker forward pass is the hog)
bash "$RUN" write FLOOR-fp32 256M,192M,160M,128M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}"
# q8 MiniLM floor — write
bash "$RUN" write FLOOR-q8 256M,192M,160M,128M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1
# lexical-only floor — query
bash "$RUN" query FLOOR-lexical 256M,192M,160M,128M,96M,64M MUNIN_EMBEDDINGS_ENABLED=false MUNIN_SQLITE_CACHE_KIB=512 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1

echo "########## CONCURRENT (sustained burst) — finalists ##########"
# The burst is where anon peaks. Run finalist configs under concurrent load at
# 512M and 320M. 15-query + 50-entry backlog (and a heavier 25/100 variant).
bash "$RUN" concurrent CONC-fp32 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}" RAMFIT_BURST_QUERIES=15 RAMFIT_BURST_BACKLOG=50
bash "$RUN" concurrent CONC-q8 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1 RAMFIT_BURST_QUERIES=15 RAMFIT_BURST_BACKLOG=50
bash "$RUN" concurrent CONC-static 1024M,512M,320M MUNIN_EMBEDDINGS_MODEL=$STATIC MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1 RAMFIT_BURST_QUERIES=15 RAMFIT_BURST_BACKLOG=50
# Heavier burst (25 queries + 100 backlog) on fp32 + q8 — the 15/50 burst may understate anon.
bash "$RUN" concurrent CONC-fp32-heavy 512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_BATCH_SIZE=4 "${SQLITE_DEFAULT[@]}" RAMFIT_BURST_QUERIES=25 RAMFIT_BURST_BACKLOG=100
bash "$RUN" concurrent CONC-q8-heavy 512M,320M MUNIN_EMBEDDINGS_MODEL=$MINILM MUNIN_EMBEDDINGS_DTYPE=q8 MUNIN_EMBEDDINGS_BATCH_SIZE=1 MUNIN_SQLITE_CACHE_KIB=1024 MUNIN_SQLITE_MMAP_BYTES=0 OMP_NUM_THREADS=1 RAMFIT_BURST_QUERIES=25 RAMFIT_BURST_BACKLOG=100

echo "############ RAM-FIT MATRIX DONE $(date -u +%FT%TZ) ############"
